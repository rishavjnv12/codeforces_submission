typedef pair<int, int> node;
typedef tree<node, null_type, less<node>,rb_tree_tag, tree_order_statistics_node_update> ordered_set;
void solve(){
	int n;read(n);
	vi a(n);read(a);
	ordered_set st;
	int ans = 0;
	for(int i=0;i<n;i++){
		int lesser = st.order_of_key({a[i],0});
		int greater = i - st.order_of_key({a[i],n});
		ans += min(lesser,greater);
		st.insert({a[i],i});
	}
	cout << ans << "\n";
}
